const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  age: {
    type: Number,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true // Ensure uniqueness of email addresses
  },
  phone: {
    type: String,
    required: true
  },
  jobTitle: {
    type: String,
    required: true
  },
  experience: {
    type: String,
    required: true
  },
  education: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  }
});

module.exports = userSchema;
